def make_variant(question):
    if question.endswith("是什么？"):
        return question[:-4] + "的标准表述是什么？"
    if question.endswith("有哪些？"):
        return question[:-4] + "主要包括什么？"
    if question.endswith("是什么"):
        return question[:-3] + "的标准表述是什么？"
    if question.endswith("关系是什么？"):
        return "关于" + question[:-1] + "，应当如何表述？"
    return "关于" + question.rstrip("？") + "，应当如何表述？"


def pad_to_500():
    seen = {(c["chapter"], c["question"]) for c in cards}
    base = list(cards)
    idx = 0
    while len(cards) < 500:
        card = base[idx % len(base)]
        q = make_variant(card["question"])
        key = (card["chapter"], q)
        if key not in seen:
            seen.add(key)
            cards.append(
                {
                    "id": len(cards) + 1,
                    "source_pdf": card["source_pdf"],
                    "chapter": card["chapter"],
                    "question": q,
                    "answer": card["answer"],
                }
            )
        idx += 1


pad_to_500()

